    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
         try {
        String name = txtName.getText();
        double price = Double.parseDouble(txtPrice.getText());

        var repository = new ProductRepository();
        var saveProduct = new SaveProductCommandHandler(repository);

        try {
            Product existingProduct = repository.findProductByName(name);
            JOptionPane.showMessageDialog(this, "El producto con nombre '" + name + "' ya existe.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } catch (Exception ex) {
            
        }

        var product = new Product(name, price);
        var total = saveProduct.createProduct(product);

        JOptionPane.showMessageDialog(this, "Producto guardado. Total de productos: " + total);
        
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "El precio debe ser un número válido.", "Error de formato", JOptionPane.ERROR_MESSAGE);
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSaveActionPerformed

