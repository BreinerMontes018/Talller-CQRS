    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
        String searchCriterio = txtSearch.getText();
        
        var repository = new ProductRepository();
        var queryHandler = new GetProductQueryHandler(repository);

        Product product = null;
        String resultText = "";
       
        try {
            int productId = Integer.parseInt(searchCriterio);
            product = queryHandler.GetProduct(productId);
        } catch (NumberFormatException e) {
            // Si no es un número lo busco por el nombre
            product = queryHandler.GetProduct(searchCriterio);
        }

        // Si se encuentra el producto se muestran sus detalles en el JTextArea
        if (product != null) {
            resultText = "Producto encontrado:\n";
            resultText += "ID: " + product.getId() + "\n";
            resultText += "Nombre: " + product.getName() + "\n";
            resultText += "Precio: " + product.getPrice() + "\n";
        } else {
            resultText = "Producto no encontrado.";
        }
        
        txtAreaResults.setText(resultText);

    } catch (Exception ex) {
        txtAreaResults.setText("Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
       
        int productId = Integer.parseInt(txtEditId.getText());
        String name = txtEditName.getText();
        double price = Double.parseDouble(txtEditPrice.getText());

        var repository = new ProductRepository();
        var editProductHandler = new EditProductCommandHandler(repository);
        
        var product = new Product();
        product.setId(productId); // El ID no puede cambiar
        product.setName(name);
        product.setPrice(price);

       
        editProductHandler.editProduct(product);

        // Muestro los detalles actualizados del producto
        Product updatedProduct = repository.findProductById(productId);
        JOptionPane.showMessageDialog(this, 
            "Producto actualizado:\nID: " + updatedProduct.getId() + 
            "\nNombre: " + updatedProduct.getName() + 
            "\nPrecio: $" + updatedProduct.getPrice());

    } catch (Exception ex) {
        
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnEditActionPerformed

